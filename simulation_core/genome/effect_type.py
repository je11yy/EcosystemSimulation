from enum import Enum


class GeneEffectType(str, Enum):
    # =========================
    # БАЗОВЫЕ ХАРАКТЕРИСТИКИ
    # =========================

    MAX_HP = "MAX_HP"
    # Влияет на максимальное здоровье агента.
    # Итоговое max_hp считается как base_max_hp * weight.
    # Значение max_hp не должно опускаться ниже 1.

    STRENGTH = "STRENGTH"
    # Влияет на физическую силу агента.
    # Используется в охоте и конфликтах как множитель базовой силы.
    # Итоговая сила не должна опускаться ниже 1.

    DEFENSE = "DEFENSE"
    # Влияет на защиту агента.
    # Снижает получаемый урон в конфликтах и охоте.
    # Итоговая защита не должна опускаться ниже 1.

    METABOLISM = "METABOLISM"
    # Влияет на скорость обмена веществ.
    # Может ускорять или замедлять рост голода за тик.
    # Позже можно связать высокий метаболизм с бонусом активности.

    # =========================
    # БАЗОВОЕ ПОВЕДЕНИЕ
    # =========================

    HUNGER_DRIVE = "HUNGER_DRIVE"
    # Склонность искать и потреблять пищу.
    # Повышает вероятность действия EAT.
    # Может усиливать реакцию на высокий hunger.

    DISPERSAL_DRIVE = "DISPERSAL_DRIVE"
    # Склонность покидать текущую территорию.
    # Повышает вероятность действия MOVE.
    # Помогает расселению популяции по графу территорий.

    SITE_FIDELITY = "SITE_FIDELITY"
    # Склонность оставаться на текущей территории.
    # Противоположна DISPERSAL_DRIVE.
    # Может повышать REST и снижать MOVE при нормальных условиях.

    REPRODUCTION_DRIVE = "REPRODUCTION_DRIVE"
    # Склонность к спариванию.
    # Повышает вероятность действия MATE.
    # Может ослабляться голодом, низким hp и плохой средой.

    # =========================
    # СРЕДА
    # =========================

    HEAT_RESISTANCE = "HEAT_RESISTANCE"
    # Устойчивость к высокой температуре.
    # Снижает штрафы от жары.
    # Повышает выживаемость и удовлетворенность в жарких территориях.

    COLD_RESISTANCE = "COLD_RESISTANCE"
    # Устойчивость к низкой температуре.
    # Снижает штрафы от холода.
    # Повышает выживаемость и удовлетворенность в холодных территориях.

    # =========================
    # КОНФЛИКТЫ И ОХОТА
    # =========================

    AGGRESSION_DRIVE = "AGGRESSION_DRIVE"
    # Склонность вступать в конфликты за ресурсы.
    # Повышает вероятность агрессивного поведения при нехватке еды,
    # высокой плотности популяции или конкуренции на территории.

    PREDATION_DRIVE = "PREDATION_DRIVE"
    # Склонность к охоте.
    # Повышает вероятность действия HUNT.
    # Может активироваться сильнее при голоде и нехватке еды.

    CARNIVORE_DIGESTION = "CARNIVORE_DIGESTION"
    # Эффективность получения пользы от успешной охоты.
    # Увеличивает восстановление hunger и hp после поедания добычи.
    # Работает как усилитель результата HUNT.

    CANNIBAL_TOLERANCE = "CANNIBAL_TOLERANCE"
    # Терпимость к нападению на генетически похожих особей.
    # Увеличивает вероятность выбрать похожую особь целью охоты.
    # Особенно важна, если в популяции много родственных агентов.

    # =========================
    # СОЦИАЛЬНОСТЬ
    # =========================

    SOCIAL_TOLERANCE = "SOCIAL_TOLERANCE"
    # Терпимость к большому количеству особей на одной территории.
    # Снижает агрессию и дискомфорт при высокой плотности.
    # Может уменьшать желание покинуть перенаселенную территорию.

    MUTATION_RATE = "MUTATION_RATE"
    # Склонность генома к мутациям при рождении потомства.
    # Работает как множитель базовых вероятностей мутаций.
    # Значения выше 1 делают потомство изменчивее, ниже 1 - стабильнее.
