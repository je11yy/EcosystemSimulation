from enum import Enum


class GeneEffectType(str, Enum):
    # =========================
    # ПИТАНИЕ И ЭНЕРГИЯ
    # =========================

    HUNGER_DRIVE = "HUNGER_DRIVE"
    # усиливает стремление искать и потреблять пищу
    # ↑ вероятность действия EAT
    # ↑ чувствительность к голоду

    SMALL_APPETITE = "SMALL_APPETITE"
    # противоположность HUNGER_DRIVE
    # ↓ стремление к еде
    # ↓ частота питания

    FOOD_OPPORTUNISM = "FOOD_OPPORTUNISM"
    # склонность есть доступную еду "здесь и сейчас"
    # ↓ стратегическое планирование
    # ↑ локальное потребление пищи

    LOW_ACTIVITY = "LOW_ACTIVITY"
    # общий тормоз активности
    # ↑ REST
    # ↓ MOVE, HUNT, FIGHT

    # =========================
    # ПЕРЕМЕЩЕНИЕ
    # =========================

    RISK_MOVE = "RISK_MOVE"
    # склонность выбирать рискованные перемещения
    # ↑ вероятность идти в менее выгодные/опасные территории

    DISPERSAL_DRIVE = "DISPERSAL_DRIVE"
    # общее стремление покидать текущую территорию
    # ↑ MOVE

    SITE_FIDELITY = "SITE_FIDELITY"
    # противоположность DISPERSAL_DRIVE
    # ↑ оставаться на текущей территории

    # =========================
    # РАЗМНОЖЕНИЕ
    # =========================

    REPRODUCTION_DRIVE = "REPRODUCTION_DRIVE"
    # повышает желание спариваться
    # ↑ вероятность MATE

    MATE_SELECTIVITY = "MATE_SELECTIVITY"
    # делает особь более "привередливой"
    # ↓ вероятность MATE при плохих условиях
    # учитывает:
    # - голод
    # - температуру
    # - генетическую совместимость

    PARENTAL_INVESTMENT = "PARENTAL_INVESTMENT"
    # влияет на поведение после размножения
    # можно использовать позже:
    # - снижение активности
    # - защита потомства

    # =========================
    # ТЕМПЕРАТУРА И СРЕДА
    # =========================

    HEAT_RESISTANCE = "HEAT_RESISTANCE"
    # снижает штраф от высокой температуры
    # ↑ выживаемость в жаре

    COLD_RESISTANCE = "COLD_RESISTANCE"
    # снижает штраф от низкой температуры
    # ↑ выживаемость в холоде

    HABITAT_SATISFACTION = "HABITAT_SATISFACTION"
    # усиливает реакцию на "подходящесть" среды
    # ↑ влияние температуры и ресурсов на выбор действий

    # =========================
    # КОНФЛИКТЫ
    # =========================

    DEFENSE_RESPONSE = "DEFENSE_RESPONSE"
    # ↑ защита в бою
    # ↓ получаемый урон

    ESCAPE_RESPONSE = "ESCAPE_RESPONSE"
    # ↑ склонность избегать конфликтов
    # ↑ вероятность отступления

    AGGRESSION_DRIVE = "AGGRESSION_DRIVE"
    # ↑ склонность вступать в конфликты
    # ↑ вероятность атакующих действий

    # =========================
    # ХИЩНИЧЕСТВО
    # =========================

    PREDATION_DRIVE = "PREDATION_DRIVE"
    # базовая склонность к охоте
    # ↑ вероятность HUNT
    # активируется сильнее при:
    # - голоде
    # - нехватке еды

    CARNIVORE_DIGESTION = "CARNIVORE_DIGESTION"
    # ↑ польза от поедания другой особи
    # влияет на восстановление hunger/hp

    CANNIBAL_TOLERANCE = "CANNIBAL_TOLERANCE"
    # снижает внутренний "штраф" за каннибализм
    # работает только если есть PREDATION_DRIVE

    # =========================
    # СОЦИАЛЬНОСТЬ
    # =========================

    SOCIAL_TOLERANCE = "SOCIAL_TOLERANCE"
    # ↑ терпимость к другим особям рядом
    # ↓ агрессия при высокой плотности

    KIN_AVOIDANCE = "KIN_AVOIDANCE"
    # склонность избегать близких генетически особей
    # можно использовать позже для:
    # - расселения
    # - избегания инбридинга
